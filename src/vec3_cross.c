#include "libmath_vec3.h"

t_vec3				vec3_cross(t_vec3 a, t_vec3 b)
{
	t_vec3			r;

	r.x = a.y * b.y - a.z * b.y;
	r.y = a.z * b.x - a.x * b.z;
	r.z = a.x * b.y - a.y * b.x;
	return (r);
}
